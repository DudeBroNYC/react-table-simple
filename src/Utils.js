const range = len => {
  const arr = [];
  for (let i = 0; i < len; i++) {
    arr.push(i);
  }
  return arr;
};

const newPerson = () => {
  //const statusChance = Math.abs(-4.7);
  //const statusChance = Math.random();
  return {
    firstName: "Joe",
    lastName: "Smith",
    wins: Math.abs(6),
    losses: Math.abs(1)
  };
};

export function makeData(len = 5) {
  return range(len).map(d => {
    return {
      ...newPerson(),
      children: range(10).map(newPerson)
    };
  });
}

